}

+ (UIColor *)coldColor {
    return [UIColor colorWithRed:86/255.0 green:181/255.0 blue:234/255.0 alpha:1.0];
}

+ (UIColor *)leafColor {
    return [UIColor colorWithRed:85/255.0 green:185/255.0 blue:73/255.0 alpha:1.0];
}

@end
